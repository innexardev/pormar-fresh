export function isNavActive(path: string, href: string) {
  return path === href || (href !== '/dashboard' && path.startsWith(`${href}/`));
}

export function getPageTitle(path: string) {
  if (path.startsWith('/pedidos/separacao')) return 'Lista de separação';
  if (path.startsWith('/entregador')) return 'App entregador';
  for (const group of NAV_GROUPS) {
    for (const item of group.items) {
      if (isNavActive(path, item.href)) return item.label;
    }
  }
  return 'Pomar Fresh';
}

export function flattenNavItems() {
  return NAV_GROUPS.flatMap((g) => g.items);
}
